#!/usr/bin/env python3
"""
Randomly search for a small pool of prompts whose average accept length is low,
then expand that pool into a benchmark dataset with repeated requests.

Typical usage:
    python test/speculative/low_accept/build_random_low_accept_dataset.py \
        --probe-metrics dataset/low_accept_gsm8k_mix/probe_metrics.json \
        --target-size 1000 \
        --unique-samples 16 \
        --candidate-pool-size 64 \
        --trials 5000 \
        --output dataset/low_accept_gsm8k_mix/random_low_accept_1k.json
"""

from __future__ import annotations

import argparse
import json
import math
import random
from copy import deepcopy
from pathlib import Path
from typing import Dict, List, Sequence


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build a low-accept-length dataset by random subset search.")
    parser.add_argument(
        "--probe-metrics",
        default="dataset/low_accept_gsm8k_mix/probe_metrics.json",
        help="Path to probe_metrics.json generated by select_low_accept_dataset.py",
    )
    parser.add_argument("--target-size", type=int, default=1000, help="Final number of requests in the exported dataset.")
    parser.add_argument(
        "--unique-samples",
        type=int,
        default=16,
        help="How many unique prompts to keep in the final repeated dataset.",
    )
    parser.add_argument(
        "--candidate-pool-size",
        type=int,
        default=64,
        help="Random search only draws from the globally lowest N probe rows.",
    )
    parser.add_argument("--trials", type=int, default=5000, help="Number of random subset search trials.")
    parser.add_argument(
        "--datasets",
        nargs="*",
        default=None,
        help="Optional dataset allowlist, e.g. truthfulqa openbookqa quartz",
    )
    parser.add_argument(
        "--max-token-threshold",
        type=float,
        default=None,
        help="Optional upper bound on mtp_avg_token_per_step before entering the candidate pool.",
    )
    parser.add_argument(
        "--max-verify-threshold",
        type=float,
        default=None,
        help="Optional upper bound on mtp_avg_verify_tokens_per_step before entering the candidate pool.",
    )
    parser.add_argument("--seed", type=int, default=0, help="Random seed.")
    parser.add_argument("--output", required=True, help="Export path for the constructed dataset.")
    return parser.parse_args()


def load_probe_rows(path: Path) -> List[dict]:
    with path.open("r", encoding="utf-8") as f:
        data = json.load(f)
    if not isinstance(data, list):
        raise ValueError(f"{path} must be a JSON list.")
    return data


def filter_rows(rows: Sequence[dict], args: argparse.Namespace) -> List[dict]:
    filtered = []
    for row in rows:
        if args.datasets is not None and row["dataset"] not in args.datasets:
            continue
        if args.max_token_threshold is not None and float(row["mtp_avg_token_per_step"]) > args.max_token_threshold:
            continue
        if args.max_verify_threshold is not None and float(row["mtp_avg_verify_tokens_per_step"]) > args.max_verify_threshold:
            continue
        filtered.append(row)
    return filtered


def score_subset(rows: Sequence[dict]) -> Dict[str, float]:
    token_values = [float(row["mtp_avg_token_per_step"]) for row in rows]
    verify_values = [float(row["mtp_avg_verify_tokens_per_step"]) for row in rows]
    return {
        "avg_token": sum(token_values) / len(token_values),
        "avg_verify": sum(verify_values) / len(verify_values),
        "min_token": min(token_values),
        "max_token": max(token_values),
    }


def search_best_subset(rows: Sequence[dict], unique_samples: int, trials: int, seed: int) -> List[dict]:
    if len(rows) < unique_samples:
        raise ValueError(f"Need at least {unique_samples} candidate rows, but only found {len(rows)}.")

    rng = random.Random(seed)
    best_subset = None
    best_key = None

    for _ in range(trials):
        subset = rng.sample(list(rows), unique_samples)
        stats = score_subset(subset)
        # Prioritize lower accept length, then lower verify length, then tighter max.
        key = (stats["avg_token"], stats["avg_verify"], stats["max_token"])
        if best_key is None or key < best_key:
            best_key = key
            best_subset = subset

    assert best_subset is not None
    return sorted(
        best_subset,
        key=lambda row: (
            float(row["mtp_avg_token_per_step"]),
            float(row["mtp_avg_verify_tokens_per_step"]),
            row["dataset"],
            int(row["sample_index"]),
        ),
    )


def expand_subset(subset: Sequence[dict], target_size: int, seed: int) -> List[dict]:
    rng = random.Random(seed)
    repeated_rows = []
    repeats_per_item = target_size // len(subset)
    remainder = target_size % len(subset)

    for subset_index, row in enumerate(subset):
        repeat_count = repeats_per_item + (1 if subset_index < remainder else 0)
        for repeat_index in range(repeat_count):
            item = deepcopy(row["source_item"])
            item["_random_low_accept_source"] = {
                "subset_index": subset_index,
                "repeat_index": repeat_index,
                "request_id": row["request_id"],
                "dataset": row["dataset"],
                "sample_index": row["sample_index"],
                "mtp_avg_token_per_step": row["mtp_avg_token_per_step"],
                "mtp_avg_verify_tokens_per_step": row["mtp_avg_verify_tokens_per_step"],
                "accepted_draft_tokens_per_step": row["accepted_draft_tokens_per_step"],
            }
            repeated_rows.append(item)

    rng.shuffle(repeated_rows)
    return repeated_rows


def main() -> None:
    args = parse_args()
    probe_path = Path(args.probe_metrics)
    output_path = Path(args.output)
    meta_path = output_path.with_suffix(output_path.suffix + ".meta.json")

    rows = load_probe_rows(probe_path)
    rows = filter_rows(rows, args)
    rows = sorted(
        rows,
        key=lambda row: (
            float(row["mtp_avg_token_per_step"]),
            float(row["mtp_avg_verify_tokens_per_step"]),
            row["dataset"],
            int(row["sample_index"]),
        ),
    )
    rows = rows[: min(args.candidate_pool_size, len(rows))]
    best_subset = search_best_subset(rows, args.unique_samples, args.trials, args.seed)
    dataset_rows = expand_subset(best_subset, args.target_size, args.seed)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", encoding="utf-8") as f:
        json.dump(dataset_rows, f, ensure_ascii=False, indent=2)

    subset_stats = score_subset(best_subset)
    metadata = {
        "probe_metrics": str(probe_path),
        "target_size": args.target_size,
        "unique_samples": args.unique_samples,
        "candidate_pool_size": args.candidate_pool_size,
        "trials": args.trials,
        "seed": args.seed,
        "dataset_allowlist": args.datasets,
        "max_token_threshold": args.max_token_threshold,
        "max_verify_threshold": args.max_verify_threshold,
        "selected_subset_stats": subset_stats,
        "selected_subset": [
            {
                "request_id": row["request_id"],
                "dataset": row["dataset"],
                "sample_index": row["sample_index"],
                "mtp_avg_token_per_step": row["mtp_avg_token_per_step"],
                "mtp_avg_verify_tokens_per_step": row["mtp_avg_verify_tokens_per_step"],
                "accepted_draft_tokens_per_step": row["accepted_draft_tokens_per_step"],
            }
            for row in best_subset
        ],
        "output_dataset": str(output_path),
    }
    with meta_path.open("w", encoding="utf-8") as f:
        json.dump(metadata, f, ensure_ascii=False, indent=2)

    print(f"Exported dataset: {output_path}")
    print(f"Exported metadata: {meta_path}")
    print(
        "Selected subset stats: "
        f"avg_token={subset_stats['avg_token']:.4f}, "
        f"avg_verify={subset_stats['avg_verify']:.4f}, "
        f"min_token={subset_stats['min_token']:.4f}, "
        f"max_token={subset_stats['max_token']:.4f}"
    )


if __name__ == "__main__":
    main()
